L.fr.formatMenuLabel = function(d1, m1, d2, m2)
    return string.format("%02d/%02d - %02d/%02d", d1, m1, d2, m2)
end

L.fr.formatLastMenuLabel = function(d1, m1)
    return string.format("%02d/%02d et +", d1, m1)
end

L.fr.formatSubtitle = function(d, m)
    return "\195\128 compter du " .. d .. " " .. L.fr.monthAbbr[m]
end

L.fr.formatWeekTitle = function(weekNum, d1, m1, d2, m2)
    local months = L.fr.monthAbbr
    if not d2 then
        return string.format("Semaine %d : %d %s et après", weekNum, d1, months[m1])
    elseif m1 == m2 then
        return string.format("Semaine %d : %d - %d %s", weekNum, d1, d2, months[m2])
    else
        return string.format("Semaine %d : %d %s - %d %s", weekNum, d1, months[m1], d2, months[m2])
    end
end

